const delay = (ms: number) => new Promise<void>((resolve) => setTimeout(resolve, ms));

export const api = {
  listHosts(): Promise<Host[]> {
    return delay(60).then(() => hosts.slice());
  },
  getHost(id: string): Promise<Host | null> {
    return delay(40).then(() => hosts.find((h) => h.id === id) || null);
  },
  getHostSnapshot(hostId: string): Promise<HostSnapshot | null> {
    return delay(40).then(() => hostSnapshots[hostId] || null);
  },
  getDecisionGroups(hostId: string): Promise<DecisionGroup[]> {
    return delay(60).then(() => decisionGroups[hostId] || []);
  },
  listAdvisories(): Promise<Advisory[]> {
    return delay(60).then(() => advisories.slice());
  },
  listProductStreams(): Promise<ProductStream[]> {
    return delay(60).then(() => productStreams.slice());
  },
  listSourceSyncs(): Promise<SourceSync[]> {
    return delay(40).then(() => sourceSyncs.slice());
  },
  getOverview(): Promise<{
    total_hosts: number;
    need_attention: number;
    reboot_queue: number;
    unknown_investigate: number;
    total_advisories: number;
    total_streams: number;
  }> {
    return delay(60).then(() => ({
      total_hosts: hosts.length,
      need_attention: hosts.filter((h) => h.overall_action !== "none").length,
      reboot_queue: hosts.filter((h) => h.needs_reboot > 0).length,
      unknown_investigate: hosts.filter((h) => h.overall_action === "investigate").length,
      total_advisories: advisories.length,
      total_streams: productStreams.length,
    }));
  },
};
